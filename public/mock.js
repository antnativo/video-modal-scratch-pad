document.getElementById("ntv_toggle").addEventListener("click", function(e){
    document.getElementById("ntv-share-area").classList.toggle("ntv_hide");
    document.getElementById("ntv-share-background").classList.toggle("ntv_modalHide");
},false);
document.getElementById("ntv_cancel").addEventListener("click", function(e){
    document.getElementById("ntv-share-area").classList.toggle("ntv_hide");
    document.getElementById("ntv-share-background").classList.toggle("ntv_modalHide");
},false);
